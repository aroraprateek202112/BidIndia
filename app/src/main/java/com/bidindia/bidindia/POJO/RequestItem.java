public class RequestItem {
    int bidWiningAmount;
    int bidCollectedAmount;
    int quantityOfBid;

    public int getBidWiningAmount() {
        return bidWiningAmount;
    }

    public void setBidWiningAmount(int bidWiningAmount) {
        this.bidWiningAmount = bidWiningAmount;
    }

    public int getBidCollectedAmount() {
        return bidCollectedAmount;
    }

    public void setBidCollectedAmount(int bidCollectedAmount) {
        this.bidCollectedAmount = bidCollectedAmount;
    }

    public int getQuantityOfBid() {
        return quantityOfBid;
    }

    public void setQuantityOfBid(int quantityOfBid) {
        this.quantityOfBid = quantityOfBid;
    }
}
